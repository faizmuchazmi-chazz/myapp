	function getRandomQuote()
	{
		$quotes = get_quotes();
		$txt = "====================\n";
		$txt .= "_*" . '✨✨✨ "' . $quotes[array_rand($quotes)][rand(0, 1) ? 'english' : 'indonesian'] . '"' . "*_\n";
		$txt .= "====================";
		return $txt;
	}
